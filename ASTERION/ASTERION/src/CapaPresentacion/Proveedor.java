            String dia = String.valueOf(cal.DATE);
           
            stFecha = anio+" / "+mes+" / "+dia;
            lbFecAltProv.setText(stFecha);
           
           
           stStatusProv="EN EVALUACION";
           lbStatusProv.setText(stStatusProv);           
        
         
           /* Indica los valores respectivos de los radiobuttons, que corresponden a los valores de stEstadoProv */
