    public void ConsultarProveedor_Simple()
    {
       
        try
        {  
            con = DriverManager.getConnection(url,usuario,contrasena);
            stmt = con.createStatement();
            String selectQuery = "sp_proveedor_TodosLosDatos"+tbClave.getText()+","+tbNombre.getText();
            PreparedStatement ps = con.prepareStatement(selectQuery);
            rs=ps.executeQuery();
            while (rs.next() ) 
            {
            modelo.insertRow(0,new Object[]{rs.getString(1),rs.getString(2)});
            }
        }
        catch (Exception ExShowSelect)
        {System.out.println("Error al insertar datos en Jtable tabla" + ExShowSelect.getMessage());}
        
    }
    
    
      
    
